# Ternary Operator
# if else condition
# if your age is grater than 18 you can watch a movie
# if your age is less than 18 you are not allowed to watch a movie

a = 5
b = 10

max_val = a if a > b else b
print(max_val)

age = int (input(" Please enter your age: \n"))

