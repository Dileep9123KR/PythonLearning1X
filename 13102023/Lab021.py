# Operators

# Comparison Operators  - usually returns bool values (true or false)

'''
 == Equal to
 != Not Equal to
 < Less than
 > Greater than
 <= Less than or Equal  to
 >= greater than or Equal to
'''

a = int(input("Enter a number or value for a: \n"))
b = int(input("Enter a number or value for b: \n"))

print(a == b)
print(a != b)
print(a < b)
print(a > b)
print(a <= b)
print(a >= b)
