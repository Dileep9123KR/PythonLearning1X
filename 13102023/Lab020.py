a = int (input("Enter a number or value for a: \n"))
b = int (input("Enter a number or value for b: \n"))

print(a+b)
print(a-b)
print(a*b)
print(a/b)
print(a**b) # example a = 10, b = 3 -> 10x10x10 (10^3)
print(a//b) # returns the quotient value
print(a%b)  # returns remainder value


