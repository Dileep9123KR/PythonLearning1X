# Operators

# Assignment Operators
'''
= Assignment
+= Add and assign
-= Subtract and assign
*= Multiply and assign
/= Divide and assign
%= Modulus and assign
**= Exponent and assign
//= Floor divide and assign
'''
a = 5
a += 2 # a = a+2 (increased by 2)
print(a)
a -= 1 # a = a-1 (decreased by 1)
print(a)
a *= 3 # a = a*3 (multiplied by 2)
print(a)
a /= 2 # a = a/2 (divided by 2)
print(a)
a %= 2 # a = a%2 (Modulus by 2)
print(a)
a **= 3 # a = a**3 (a^3)
print(a)
a //= 3 # a = a-1 (Floor divide by 3)
print(a)